public static void Insercion (int[] numbers) {
      for (int i=1; i < numbers.length; i++) {
         int aux = numbers[i];
          int pos = i;
         while ((pos > 0) && (numbers[pos - 1] > aux)){
              numbers[pos] = numbers[pos -1];
              pos --;
          }
          numbers[pos]=aux;
      }
    }//GEN-LAST:event_btnSaveActionPerformed

